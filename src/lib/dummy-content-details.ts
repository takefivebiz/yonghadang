/**
 * slug로 ContentDetail 조회
 * TODO: [백엔드 연동] /api/contents/[slug] 실제 호출로 교체
 */
export const getContentDetail = (slug: string): ContentDetail | null => {
  const base = DUMMY_CONTENTS.find((c) => c.slug === slug);
  const extras = DETAIL_EXTRAS[slug];
  if (!base || !extras) return null;
  return { ...base, ...extras };
};
